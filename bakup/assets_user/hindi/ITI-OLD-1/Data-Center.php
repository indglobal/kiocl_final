		  <h3 class="backCorporate" style="text-align:left; padding-left:2%;">Telecom Secretary inaugurates ITI’s Next Generation Data Center</h3>
			<p class="text-justify"><strong>Bangalore, June 30:</strong></p>
			
            <p class="text-justify">Mr. Siddhartha Behura, IAS, Chairman, Telecom Commission and Secretary, Telecom, Government of India inaugurated the State-of-the-art Next Generation Data Center of ITI Limited (ITI) at its Campus in Dooravaninagar here today. Equipped with world-class facilities, it is aimed towards providing Customer-centric Services to the Government, as well as Private Enterprises. “It is an important landmark for ITI and this will change the outlook towards Data Center Services in the country”, said Mr. S.K.Chatterjee, CMD, ITI. 
<br>
<br>
The center is setup by ITI in collaboration with Trimax Data Center Services Limited, Mumbai (Trimax).
<br>
<br>
Speaking on the occasion, Surya Prakash Madrecha, Chairman, Trimax said, "We are proud to be associated with ITI for this prestigious project. This partnership will help achieve leadership and greater success in Data Center business in India and abroad in the future”. 
<br>
<br>
The ITI Data Center is planned over an area of 1, 20,000 sq feet. The existing facility occupies 75,000 sq feet and the rest of the expansion will take place in phase II by the end of this year. It will have an edge over other Data Centers not only through its complete range of data center services but also in terms of its infrastructure, effective and eco-friendly fire suppression facilities, its high level of security, carrier neutrality, availability of power, precision controlled environment and 24 x 7 support services. 
<br>
<br>
ITI Data Center offers a wide range of services including Co-Location Services, Hosting Services and Managed Services such as Server Management, Network Management, Security Management, Storage Management etc., The Value added Services such as Messaging solutions, Video Conferencing, and Remote Infrastructure Management through its state-of-the-art Data Center Operating Center will be the prime business focus of ITI Data Center.
<br>
<br>
ITI is in the final discussions with RailTel New Delhi, Karnataka Police Housing Ltd Bangalore, Business Intelligent Technologies (BIT) Bangalore, Glodyne Mumbai, Madhucon Hyderabad etc. for business association of Data Center Services. Encouraged by the overwhelming market response, ITI targets a turnover of Rs. 50 crore in the first year of its operations and expects to generate much higher revenue in the future. ITI has plans to set up such Data Centers at other locations across the country and by networking them intends to offer Pan-India Data Center Services.</p>

