           <h3 class="backCorporate" style="text-align:left; padding-left:2%;">ITI notches-up highest turn over in seven years</h3>

				<p><strong>Bangalore, May 18, 2009:</strong></p>		
            <p class="text-justify"> The ITI Ltd has achieved a turn-over of Rs.1,829 crores for the financial year 2008-09 which is the highest for the past seven years.  Despite facing severe financial constraints, it has been a satisfying year for the company especially on the production front.  According to Mr. Ravi Agarwal, Director (Production), the manufacturing, installation and commissioning activities constituted about 82% of the turn-over while trading contributed to about 18%.
<br><br>
The company has been able to garner the support of the Government, the Ministry of Communications in particular for revival of the company which takes care of cleaning the balance sheet thereby infusing the required working capital.
<br><br>
 The company has fixed the MoU target for the year 2009-10 at Rs.5,760 crores.  “With a healthy order book position of over Rs.6,000 crores, ITI aims to achieve this MoU target”, says Mr. Agarwal.</p>
