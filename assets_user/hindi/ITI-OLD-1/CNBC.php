		 <h3 class="backCorporate" style="text-align:left; padding-left:2%;">CMD's Interview to CNBC TV18</h3>

				<p><strong>We have begun the year on strong note: CMD</strong></p>		
            <p class="text-justify"> Shri S. K. Chatterjee, CMD, ITI Ltd, has said that the Company has begun the year with a lot of optimism and is poised for changes in its business structure in the coming months. In an exclusive interview with the CNBC-TV 18, he pointed out that ITI started the current fiscal with orders worth Rs. 6,800 crore. “We have memorandum of understanding (MoU) target of Rs. 5,760 crore,” he added. 
<br>
<br>
Excerpts of the interview telecast on August 5, 2009
<br>
<br>
Q: If you could start by sharing with us where the process of disinvestment has reached at ITI – has the consultant been appointed? Who is it and have you now started the process of going ahead with formation of those three joint ventures or SPVs?
<br>
A: Yes, we have started the process. We have appointed a consultant who is likely to finalize the expression of interest very soon. We propose to take out the expression of interest by August 15 and after seeing the responses we will work up on it. 
<br><br>
Q: Who is the consultant?
<br>A: We have appointed Darashaw as a consultant.
<br><br>
Q: By August 15 expression of interest will be called for?
<br>A: Yes, because we are going for the formation of three JVCs, one in Rae Bareli, Naini and Bangalore. So, it is on that basis that we will work upon it. 
<br><br>
Q: What is the structure for these three- it is more or less a JV that you are leaning towards and will it be 50:50 between whichever company you chose to tie-up with an ITI?
<br>A: No. The controlling stake will be with the JV partner. They will have between 51 to 74 per cent stake and the remaining will be distributed. ITI will also have some stake in the joint partnership. 
<br><br>
Q: There has been an EBITDA loss this quarter as well though it has come off quite a bit from what you had in the previous quarters, is there a target for ITI to move into the black?
<br>A: Yes. We have a very strong target and we have started this year with a very strong note. We have around Rs 6800 crore orders. We have set for ourselves the MOU target of around Rs 5760 crore and so far the process has been very good inspite of the financial difficulties that we are facing now. We have reached Rs 1500 crore which by any stretch is far ahead than what we had in the last quarter this time and we propose to go ahead with these deals and with the infusion of fund which was recently announced by the government, our position will certainly improve and we hope to meet the target and even surpass the target if it is possible.  
<br><br>
Q: Once the joint ventures are formed in Bangalore, Naini, Rae Bareli do you think these plants would still be in a position to attract order from the government or from large PSUs which is the mainstay of ITI’s business?
<br>A: Yes, definitely. Because, we have seen what is going to be the market in the next few years where Wi-MAX and associated products and CPU will play very important role and therefore, we have identified that as a product. Similarly for Naini, we have GPON and GE-PON products. Also, we have identified IP core products for Bangalore. These are the areas where all the new efforts are going to be and the telecom scenario is going to attract these products on a very large basis. So, therefore once we have these manufacturing facilities here, we are sure that we will not only be able to process the government orders, we will also get heavy order from the other private players also because these are the equipments which will be required in the times to come. 
<br><br>
Q: Given the product lines that you have for these three plants, who do you think would be the likely partners who will put expression of interest? Would it be companies like Alcatel Lucent, Huawei and do you think you will get good valuation for these three properties?
<br>A: We are going to the global market. So, already we have tied-up with Alcatel Lucent and Huawei. So at this stage it will be too early to predict who will be the actual partner. But I have a feeling that given the opportunity and given the business scenario that we have in India, we will be able to attract a large number of private players and also other important players. Because, this is one area where large possibilities of business are coming. So, I am sure that large number of players will come. Apart from Alcatel with which we have already tied up, Huawei Chinese, Nokia, Ericsson, Siemens, Japanese players all will be interested to take up this manufacturing, utilize the manufacturing facility and the business opportunity.
<br><br>
Q: Are you confident that this process will go through successfully and smoothly? In the past, there has been some political sensitivity with some of your plants such as Rae Bareli?
<br>A: It will definitely work out because you simultaneously see that we have been given a package of Rs 2820 crore which takes care of the liabilities that we have. So, after having improved our position and cleaning the balance sheet, there will be interested players working with us and also as a joint venture partner. There are possibilities that we will be able to. Because, BSNL is also going a very big way on the Wi-max and other areas of GPON. They will be able to process that. In the telecom scenario, there are a lot of growth possibilities. So I see no reason why the private players or the important players will not come here and make a venture with us. They get land, infrastructure facilities, and if they want, the skilled manpower that we have with us at various units. So, it will be a win-win situation for all of us. 
<br><br>
Q: What is the eventual plan or the final game plan – would it be that once partners come in successfully for three, you will look at all six and ITI will be a minority shareholder with majority global or local partners in all of its six plants?
<br>A: ITI will continue with the business profile that we have at present. We will keep on processing the orders that we have at present and we will continue to get the orders from BSNL and MTNL and other people. These joint ventures will be independent entities. ITI will also have a stake. We will continue to explore other ventures for ITI to attract business possibilities that we have around us.</p>
